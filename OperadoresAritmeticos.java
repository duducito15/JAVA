public class OperadoresAritmeticos {
    public static void main(String[] args) {
        //Operadores aritmeticos
        /*
         * me permite establecer relaciones entre las variables
         * Suma -> +
         * Resta -> -
         * Multiplicación -> *
         * División -> /
         * Resto(división) -> %
         * incremento -> ++ (el valor de la variable de 1 en 1)
         * decremento -> -- (el valor de la variable de 1 en 1)
         */
        int numero = 127;
        
        System.out.println("Suma: "+ (numero + 5));
        System.out.println("Resta: "+ (numero - 5));
        System.out.println("Multiplicación: "+ (numero * 5));
        System.out.println("División: "+ (numero / 5));
        System.out.println("Resto: "+ (numero % 5));
    }
}
